function calculateNumbers(var1, var2) {
    console.log(var1 + var2);
}

function subtractNumbers(var1, var2) {
    console.log(var1 - var2);
}

function multiplyNumbers(var1, var2) {
    console.log(var1 * var2);
}

function divideNumbers(var1, var2) {
    if (var2 !== 0) {
        console.log(var1 / var2);
    } else {
        console.log("Cannot divide by zero");
    }
}